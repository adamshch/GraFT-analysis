function y = vec(x)

% y = vec(x)
%
% This function is a very useful function that just reshapes an N-D array
% as a vector. Useful when reshaping the output of a function without 
% creating clutter in the variable space
%
% 2020 - Adam Charles

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

y = x(:);                                                                  % Just reshape the array as a vector

end

